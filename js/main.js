function renderCart() {
  const lbl = cartLabels[lang] || cartLabels.fr;
  const body = document.getElementById('cartBody');
  const foot = document.getElementById('cartFoot');
  document.getElementById('cartTitle').textContent = lbl.title;
  document.getElementById('cartTotalLabel').textContent = lbl.total;
  document.getElementById('cartCheckout').textContent = lbl.checkout;
  document.getElementById('cartComing').textContent = lbl.coming;

  if (cart.length === 0) {
    body.innerHTML = `<div class="cart-empty"><div class="cart-empty-icon">🛒</div><p>${lbl.empty}</p></div>`;
    foot.style.display = 'none';
    return;
  }

  body.innerHTML = cart.map((item, i) => `
    <div class="cart-item">
      <div class="cart-item-img"><img src="${item.img}" alt="${item.name}"/></div>
      <div class="cart-item-info">
        <div class="cart-item-name">${item.name}</div>
        <div class="cart-item-price">USD $${item.price.toFixed(2)}</div>
        <div class="cart-item-qty">
          <button class="qty-btn" onclick="changeQty(${i},-1)">−</button>
          <span class="qty-num">${item.qty}</span>
          <button class="qty-btn" onclick="changeQty(${i},1)">+</button>
        </div>
      </div>
      <button class="cart-item-remove" onclick="removeFromCart(${i})">✕</button>
    </div>
  `).join('');

  const total = cart.reduce((s, i) => s + i.price * i.qty, 0);
  document.getElementById('cartTotalPrice').textContent = `USD $${total.toFixed(2)}`;
  foot.style.display = 'block';
}

function addCart(name, price, img) {
  const existing = cart.find(i => i.name === name);
  if (existing) {
    existing.qty++;
  } else {
    cart.push({ name, price: parseFloat(price), img: img || 'img/product-cartes.jpg', qty: 1 });
  }
  saveCart();
  updateBadge();
  launchConfetti();
  openCart();
}

function launchConfetti() {
  const colors = ['#E8B4B0','#9DB8A8','#C4A882','#F5C6A0','#fff','#E8D5C4'];
  const canvas = document.createElement('canvas');
  canvas.style.cssText = 'position:fixed;top:0;left:0;width:100%;height:100%;pointer-events:none;z-index:9999';
  canvas.width = window.innerWidth;
  canvas.height = window.innerHeight;
  document.body.appendChild(canvas);
  const ctx = canvas.getContext('2d');
  const pieces = Array.from({length: 60}, () => ({
    x: Math.random() * canvas.width,
    y: -10 - Math.random() * 40,
    r: 3 + Math.random() * 5,
    d: 1.5 + Math.random() * 2.5,
    color: colors[Math.floor(Math.random() * colors.length)],
    tilt: Math.random() * 10 - 5,
    tiltSpeed: .08 + Math.random() * .1,
    angle: 0,
    vx: (Math.random() - .5) * 3,
  }));
  let frame = 0;
  function draw() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    pieces.forEach(p => {
      p.angle += p.tiltSpeed;
      p.y += p.d;
      p.x += p.vx;
      p.tilt = Math.sin(p.angle) * 10;
      ctx.beginPath();
      ctx.ellipse(p.x, p.y, p.r, p.r * .4, p.tilt * Math.PI / 180, 0, 2 * Math.PI);
      ctx.fillStyle = p.color;
      ctx.fill();
    });
    frame++;
    if (frame < 90) requestAnimationFrame(draw);
    else canvas.remove();
  }
  draw();
}

function removeFromCart(index) {
  cart.splice(index, 1);
  saveCart();
  updateBadge();
  renderCart();
}

function changeQty(index, delta) {
  cart[index].qty += delta;
  if (cart[index].qty <= 0) cart.splice(index, 1);
  saveCart();
  updateBadge();
  renderCart();
}

